using System;

namespace Ejercicio1
{
    internal class Calculadora
    {
        public static int Sumar(int x, int y)
        {
            return x + y;
        }

        public static int Restar(int x, int y)
        {
            return x - y;
        }

        public static int Multiplicar(int x, int y)
        {
            return (x * y);
        }

        public static int Dividir(int x, int y)
        {
            return (x / y);
        }
    }
}